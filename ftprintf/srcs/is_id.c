#include "../includes/libftprintf.h"

int	is_id(char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'i'
		|| c == 'd' || c == 'u' || c == 'x'
		|| c == 'X' || c == '%')
		return (1);
	else
		return (0);
}
